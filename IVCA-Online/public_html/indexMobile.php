  <p >The International Veteran Cycle Association is dedicated to the preservation of the history of the bicycle, bicycling and the enjoyment of the bicycle as a machine.</p>
<p >
On 26<sup>th</sup> May 1986 in Lincoln, England, the International Veteran Cycle Association was formed by veteran cycle clubs, museums and collectors from around the world.
<a href="history.php">Our story began five years earlier in Grantham</a></p>
          
            <p>&nbsp;</p>
            <p style="font-weight:bold;"> 
The Objectives of the IVCA are:
</p>
            <ul class="objectives">
                <li>To encourage interests and activities relating to all old human-powered vehicles of one or more wheels deriving from the velocipede tradition.
</li>
                <li>To support and encourage research and classification of their history and to act as a communication medium between clubs, societies and museums world-wide on mutual matters relating to old cycles.
</li>
                <li>To favour by rallies, meetings, exhibitions, sales and publications, the free circulation of ideas concerning cycles, cycling and related matters throughout the world. One rally each year is designated as the International Veteran Cycle Rally (IVCR) and should aim to be the major international meeting of that year. The country hosting the rally is responsible for the format of the rally, which may comprise of four or more days of activities. The host country for the IVCR will be decided at the Annual General Meeting several years ahead Offers to host the IVCR are acceptable from any country.
</li>
                <li>To produce and circulate to its members a printed publication.
</li>

            </ul>
          
