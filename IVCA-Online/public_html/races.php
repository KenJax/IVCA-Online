<p >The Races</p>
<p >The races within IVCA are something on its own.Here you will find the popular races of the Hobby Horses and the Ordinaries or High Bicycles.Among other races you will find solid tire safeties and pneumatic tire safeties.</p>
<p >The races are very popular for the public as well as the cyclists and the heats are not as you will find it in any other races; in the IVCA races you will see transportation bicycles competing with racer bicycles; 60 years old cyclist competing with 20 years old; children competing against each other; races for tandems or special bicycles or even the popular velocar race.</p>
<p >Not two rallies are the same sometimes the distances are 1 mile or less; sometimes as in 2010 the distance was up to 8 km. Some rallies have races in many categories while other only held races among the oldest bicycles; then again sometimes as in 2007 and 2008 there are no races or as in 2008even a Century ride.</p>
<p >The Century ride is a classic 100 miles ride with alternative distances such as 75, 50 or 25 miles. From the beginning the Century ride should be ridden between sunrise and sunset but nowadays the organizers starts the Century at 06:00 o'clock and ends at 18:00 o'clock. In 2013 the organizers turned the Century into a race (only for the 100 miles); the result from this particular Century can be found <a href="http://ivca-online.org/1552702.html">here</a>.</p>
<p >In our <a href="http://ivca-online.org/1551963.html">galleries</a> you will find many pictures from the races as well as the Century.</p>
