<p>
At the end of the tour we visited the Abbey of Sa Sigismondo. One of the most 
important Italian churches of the medieval style built on the site where Bianca,
Maria Visconti and Francesco Sforza got married in 1427. The church was a fantastic and harmonious example of the 16th century 
style. The frescoes were painted by Camila Baccareina Iram between 1535 and 154,
the year of his death and were finished by brothers Giulio and Bernardino Campi.
Outside the church, a splendid display of our veteran cycles.
</p>
